    private void createNewUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createNewUserActionPerformed
        // TODO add your handling code here:
        
         /*/ Allows the Admin to add a new Staff member account and stores the data in the database /*/
        
        Connection conn = DBConnectivity.getConnection();
        Admin admin = new Admin();
        String sql = "Insert into Staff (firstName, lastName, email, username, password, jobTitle) values (?, ? , ?, ?, ?, ?)";
        String sql2 = "Insert into Mechanic (staffID, labourRate) values (?, ?)";
        String sql3 = "SELECT MAX(staffID) FROM Staff";

        try {
            PreparedStatement pst = conn.prepareStatement(sql);
            PreparedStatement pst2 = conn.prepareStatement(sql2);
            Statement statement = conn.createStatement();

             /*/ This stores all the data in the specific columns in the database./*/
            
            pst.setString(1, firstName.getText());
            pst.setString(2, surname.getText());
            pst.setString(3, email.getText());
            pst.setString(4, username.getText());
            pst.setString(5, password.getText());
            pst.setString(6, jobTitle.getSelectedItem().toString());
            pst.execute();

            ResultSet rs = statement.executeQuery(sql3);

            if (jobTitle.getSelectedItem().toString().equals("Mechanic")) {
                pst2.setString(1, rs.getString(1));
                pst2.setString(2, "105");
                pst2.execute();

            } else if (jobTitle.getSelectedItem().toString().equals("Foreperson")) {
                pst2.setString(1, rs.getString(1));
                pst2.setString(3, "125");
                pst2.execute();
            }

            /*/ When the "create" button is clicked a popup is created with the message below /*/
            
            JOptionPane.showMessageDialog(null, "Saved");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                DBConnectivity.disconnect(conn);
            } catch (Exception e) {
            }
        }
        
        /*/ For security purposes, we disconnect the database /*/
           
        DBConnectivity.disconnect(conn);
    }//GEN-LAST:event_createNewUserActionPerformed

