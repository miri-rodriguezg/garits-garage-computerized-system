    private void doneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_doneActionPerformed
        // TODO add your handling code here:
        Connection conn = DBConnectivity.getConnection();
        String sql = "Insert into Job ( VehicleregNo,  jobDescription, estimatedTime, status, jobType, setPrice, customerId) values ( ?, ?, ?, ? , ?, ?, ?)";

        try {
            PreparedStatement pst = conn.prepareStatement(sql);
            LocalDate localDate = LocalDate.now();

            pst.setString(1, regNo.getText());
            pst.setString(2, description.getText());
            pst.setString(3, estimatedTime.getText());
            pst.setString(4, status.getText());
            pst.setString(5, jobType.getSelectedItem().toString());
            pst.setString(6, price.getText());
            pst.setString(7, customerId.getText());

            pst.execute();
            JOptionPane.showMessageDialog(null, "Saved");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                DBConnectivity.disconnect(conn);
            } catch (Exception e) {
            }
        }
        DBConnectivity.disconnect(conn);
    }//GEN-LAST:event_doneActionPerformed

