    private void orderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_orderActionPerformed

//Makes a record of parts ordered in the database   
        Connection conn = DBConnectivity.getConnection();
        String sql = "Insert into partsOrdered (partNo, quantity, dateOrdered) values (?, ?, ?)";

        try {
            PreparedStatement pst = conn.prepareStatement(sql);

            LocalDate localDate = LocalDate.now();
            pst.setString(1, partNo.getText());
            pst.setString(2, quantity.getText());

            pst.setString(3, java.sql.Date.valueOf(localDate).toString());
            pst.execute();
            conn.close();
            JOptionPane.showMessageDialog(null, "Saved");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        DBConnectivity.disconnect(conn);
    }//GEN-LAST:event_orderActionPerformed

