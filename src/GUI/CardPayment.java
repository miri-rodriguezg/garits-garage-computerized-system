    public void calculateTotal() {
        Connection conn = DBConnectivity.getConnection();
        String jobNo = jobId.getText();
        String regNo = null;
        String sql = "SELECT VehicleregNo FROM Job WHERE jobNo = '" + jobNo + "'";
        try {
            Statement jobStatement = conn.createStatement();
            ResultSet jobRS = jobStatement.executeQuery(sql);
            while (jobRS.next()) {
                regNo = jobRS.getString(1);
            }
            BigDecimal grandTotal = PI.generateInvoiceTable(conn, regNo);
            total.setText("£" + grandTotal.toString());
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
        DBConnectivity.disconnect(conn);
    }

