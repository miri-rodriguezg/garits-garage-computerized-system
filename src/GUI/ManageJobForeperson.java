    public void getMechanics() {
        Connection conn = DBConnectivity.getConnection();
        String sql = "SELECT staffID FROM Staff where jobTitle = 'Mechanic' OR jobTitle = 'Foreperson'";

        try {
            Statement statement = conn.createStatement();
            ResultSet rs = statement.executeQuery(sql);

            while (rs.next()) {
                mechanic.addItem(rs.getString("staffID"));
            }
        } catch (SQLException e) {

        }
        DBConnectivity.disconnect(conn);
    }

