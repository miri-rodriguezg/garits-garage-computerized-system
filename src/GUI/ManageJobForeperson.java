    private void jobSheetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jobSheetActionPerformed
        // TODO add your handling code here:
        String jobNo = jobId.getText();
        jobNo = jobNo.trim();
        Connection conn = DBConnectivity.getConnection();
        String sql = "SELECT VehicleregNo FROM Job WHERE jobNo = '"+jobNo+"' ";
        try {
            Statement stm = conn.createStatement();
            ResultSet rs = stm.executeQuery(sql);
            while (rs.next()) {
                String regNo = rs.getString(1);
                PIOR.setRegNo(regNo);
            }
            Homepage homepage = (Homepage) SwingUtilities.getWindowAncestor(this);
            homepage.setContentPane(new JobSheet());
            homepage.setVisible(true);
        }
        catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jobSheetActionPerformed

