    private void ApplyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ApplyActionPerformed
//Set three different conditions for flexible discount and separate them via "-"
//e.g: if price is > 1000, discount would be 1% would be recorded as 1000-1
//in the invoice, we would split the value and the discount rate and make calculations with these values separately
        Connection conn = DBConnectivity.getConnection();
        String sql = "Insert into Discount (customerId, First, Second, Third ) values (? , ?, ?, ?)";

        try {
            PreparedStatement pst = conn.prepareStatement(sql);

            pst.setString(1, customerId.getText());
            pst.setString(2, first.getText());
            pst.setString(3, second.getText());
            pst.setString(4, third.getText());

            pst.execute();

            JOptionPane.showMessageDialog(null, "Saved");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
               DBConnectivity.disconnect(conn);
            } catch (Exception e) {
            }
        }
        DBConnectivity.disconnect(conn);
    }//GEN-LAST:event_ApplyActionPerformed

