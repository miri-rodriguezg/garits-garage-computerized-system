//The next 3 following methods get a screenshot of the GUI, transform it into PDF format, making it printable     
    public static BufferedImage getScreenShot(Component component) {

        BufferedImage image = new BufferedImage(1000, 500, BufferedImage.TYPE_INT_RGB);
        component.paint(image.getGraphics());

        return image;

    }

    public static void SaveScreenShot(Component component, String filename) throws Exception {

        BufferedImage img = getScreenShot(component);
        ImageIO.write(img, "jpg", new File(filename));
    }

    public static void printReminder2() throws BadElementException, IOException {
        try {
            PdfWriter writer = PdfWriter.getInstance(reminder2Doc, new FileOutputStream("Reminder2.pdf"));
            reminder2Doc.open();

            Image reminder2I = Image.getInstance("reminder2.jpg");

            reminder2Doc.add(reminder2I);

            reminder2Doc.close();
            writer.close();
        } catch (DocumentException e) {
            e.printStackTrace();
        } catch (FileNotFoundException e) {
            e.printStackTrace();
        }
    }

