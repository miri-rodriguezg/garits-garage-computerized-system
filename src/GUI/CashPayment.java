    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int intId = Integer.parseInt(jobId.getText());
        LocalDate date = LocalDate.now();

        Connection conn = DBConnectivity.getConnection();
        String sql = "Insert into Payment (jobNo, datePaid, paymentType) values ( ?, ?, 'Cash') ";
        try {
            PreparedStatement pst = conn.prepareStatement(sql);

            pst.setString(1, jobId.getText());
            pst.setString(2, date.toString());

            pst.execute();

            JOptionPane.showMessageDialog(null, "Saved");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                DBConnectivity.disconnect(conn);
            } catch (Exception e) {
            }
        }
        DBConnectivity.disconnect(conn);
    }//GEN-LAST:event_jButton2ActionPerformed

