    private void SignInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SignInActionPerformed
        // TODO add your handling code here:
        Connection conn = DBConnectivity.getConnection();
        //User user = new User();
        String userName = username.getText();
        String pass = password.getText();
        String sql = "select jobTitle from Staff where username=? and password=?";
        //sets the sql statement for this query

        try {
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, userName);//replaces the username in the query with username entered
            pst.setString(2, pass);//replaces the password in the query with password entered
            ResultSet rs = pst.executeQuery();//runs the sql statement over the connection
            rs.next(); //reads the next line in the table (so not the column titles)
            String role = rs.getString("jobTitle"); //initialised local variable to result set 

            // switch case to determine what page to be directed to next.
            switch (role) {
                case "Admin":
                    this.setContentPane(new AdminHome());
                    this.setVisible(true);
                    playWelcome("welcome.wav");
                    break;
                case "Receptionist":
                    this.setContentPane(new ReceptionistHome());
                    this.setVisible(true);
                    playWelcome("welcome.wav");
                    break;
                case "Mechanic":
                    this.setContentPane(new MechanicHome());
                    this.setVisible(true);
                    playWelcome("welcome.wav");
                    break;
                case "Foreperson":
                    this.setContentPane(new ForepersonHome());
                    this.setVisible(true);
                    playWelcome("welcome.wav");
                    break;
                case "Franchisee":
                    this.setContentPane(new FranchiseeHome());
                    this.setVisible(true);
                    playWelcome("welcome.wav");
                    break;
                default:
                    JOptionPane.showMessageDialog(null, "Error. Try Again");
                    DBConnectivity.disconnect(conn);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error. Try Again"); //Error message pops up
            //Logger.getLogger(Homepage.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                DBConnectivity.disconnect(conn);
            } catch (Exception e) {
            }
        }

    }//GEN-LAST:event_SignInActionPerformed
    public static void playWelcome(String filepath) {

        InputStream music;
        try {
            music = new FileInputStream(new File(filepath));
            AudioStream audio = new AudioStream(music);
            AudioPlayer.player.start(audio);

        } catch (Exception e) {

            System.out.println(e.getMessage());
        }
    }
    
    
