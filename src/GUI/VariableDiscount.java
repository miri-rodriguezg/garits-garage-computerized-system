    private void ApplyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ApplyActionPerformed

//Created a discount table where Variable discount rates will be inserted under MoT, Annual and Repair columns
//When calculating invoices later on, the fields which are not null in the Discount table will be used so that Fixed and Flexible are ignored
        Connection conn = DBConnectivity.getConnection();
        String sql = "Insert into Discount (customerId, MoT , Annual, Repair ) values (? , ?, ?, ?)";

        try {
            PreparedStatement pst = conn.prepareStatement(sql);

            pst.setString(1, customerId.getText());
            pst.setString(2, MoT.getText());
            pst.setString(3, Annual.getText());
            pst.setString(4, Repair.getText());

            pst.execute();

            JOptionPane.showMessageDialog(null, "Saved");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                DBConnectivity.disconnect(conn);
            } catch (Exception e) {
            }
        }
        DBConnectivity.disconnect(conn);
    }//GEN-LAST:event_ApplyActionPerformed

