    private void removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeActionPerformed

//Allows to remove Staff account by selecting the corresponding staffId
//Allows to remove Staff account from the Mechanic table too with the staffId
        Connection conn = DBConnectivity.getConnection();
        Admin admin = new Admin();
        String sql = "DELETE from Staff where staffID=?";
        String sql2 = "DELETE from Mechanic where staffID=?";

        try {
            PreparedStatement pst = conn.prepareStatement(sql);
            PreparedStatement pst2 = conn.prepareStatement(sql2);
            
            pst.setString(1, id.getText());
            pst2.setString(1, id.getText());

            pst.execute();
            pst2.execute();
            
            conn.close();
            JOptionPane.showMessageDialog(null, "Deleted");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                conn.close();
            } catch (Exception e) {

            }
        }

    }//GEN-LAST:event_removeActionPerformed

