    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:
        Connection conn = DBConnectivity.getConnection();
        String sql = "Insert into StockLedger (partName, manufacturer, vehicleType, price, quantity, treshold) values (?, ?, ?, ?, ? , ?)";

        try {
            PreparedStatement pst = conn.prepareStatement(sql);

            pst.setString(1, name.getText());
            pst.setString(2, manufacturer.getText());
            pst.setString(3, vehicleType.getText());
            pst.setString(4, price.getText());
            pst.setString(5, quantity.getText());
            pst.setString(6, threshold.getText());

            pst.execute();

            JOptionPane.showMessageDialog(null, "Saved");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                DBConnectivity.disconnect(conn);
            } catch (Exception e) {
            }
        }
        DBConnectivity.disconnect(conn);
    }//GEN-LAST:event_addActionPerformed

