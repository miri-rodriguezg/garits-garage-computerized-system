//The next 3 following methods get a screenshot of the GUI, transform it into PDF format, making it printable    
    public static BufferedImage getScreenShot(Component component) {

        BufferedImage image = new BufferedImage(1000, 700, BufferedImage.TYPE_INT_RGB);
        component.paint(image.getGraphics());

        return image;

    }

    public static void SaveScreenShot(Component component, String filename) throws Exception {

        BufferedImage img = getScreenShot(component);
        ImageIO.write(img, "jpg", new File(filename));
    }

    public static void printReminder3() throws BadElementException, IOException {
        try {
            PdfWriter writer = PdfWriter.getInstance(reminder3Doc, new FileOutputStream("Reminder3.pdf"));
            reminder3Doc.open();

            Image reminder3I = Image.getInstance("reminder3.jpg");

            reminder3Doc.add(reminder3I);

            reminder3Doc.close();
            writer.close();
        } catch (DocumentException e) {
            e.printStackTrace();
        } catch (FileNotFoundException e) {
            e.printStackTrace();
        }
    }

